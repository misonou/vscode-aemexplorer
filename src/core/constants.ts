function withPrefix(prefix: string): typeof PROP & { readonly [P in string]: string } {
    let src: Record<string, string> = PROP;
    let dst: Record<string, string> = {};
    for (let i in src) {
        dst[i] = `${prefix}/${src[i]}`;
    }
    return new Proxy(Object.freeze(dst), {
        get(t, p) {
            if (typeof p === 'string') {
                return t[p] || `${prefix}/${p}`;
            }
        }
    }) as any;
}

